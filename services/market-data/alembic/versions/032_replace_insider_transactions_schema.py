"""Replace insider_transactions with correct L-4b schema.

Revision ID: 032
Revises: 031
Create Date: 2026-06-06

WHY THIS MIGRATION EXISTS:

Migration 030 used ``CREATE TABLE IF NOT EXISTS insider_transactions`` which
silently no-op'd in every environment where the table was already present from
migration 001 (created via the EODHD Fundamentals blob endpoint with a
completely different schema: ``owner_name``, ``owner_title``,
``acquired_disposed``, ``total_shares_owned``, ``transaction_code`` columns).

As a result:
  * The ``net_value_usd`` column introduced by migration 030's DDL was never
    actually created.
  * Every execution of ``rollup_insider_90d.run_insider_rollup_once`` failed
    with ``column "net_value_usd" does not exist``.
  * The ``insider_net_buy_90d`` screener metric (INSIDER 90D) is always NULL.
  * ``InsiderTransactionsConsumer`` inserts via ``uq_insider_transactions_natural_key``
    which also didn't exist on the legacy table — so inserts were producing
    errors or rolling back silently.

FIX STRATEGY:
  1. DROP TABLE … CASCADE the old broken table. Existing rows are low-fidelity
     stub data from the Fundamentals blob endpoint — not the per-transaction
     EODHD feed. No data worth preserving; the InsiderTransactionsConsumer will
     repopulate from the next S2 fetch cycle.
  2. CREATE TABLE with the full correct L-4b schema from migration 030's
     intended DDL (which was never applied).
  3. Recreate the composite index.

Downgrade: drop the L-4b table and bring back the legacy 001-era schema so
the DB is at least consistent (though no data is restored — this is dev-only).

FORWARD COMPAT (R11):
  The L-4b table schema adds ``net_value_usd`` and ``source`` compared to the
  legacy 001 schema. No existing code reads the old columns (``owner_name``,
  ``acquired_disposed``, etc.) — those fields were only ever accessed by the
  old FundamentalsConsumer which already ignores this table.

IDEMPOTENCY:
  Uses ``DROP TABLE IF EXISTS … CASCADE`` and ``CREATE TABLE`` (not IF NOT
  EXISTS — we want a hard failure if the table somehow already exists with the
  correct schema, so a re-run is detected rather than silently ignored).
"""

