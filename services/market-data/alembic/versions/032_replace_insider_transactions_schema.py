    # The legacy 001-era table has no FK referencing columns that matter for
    # other tables — CASCADE is precautionary.
    # We do NOT use ``IF EXISTS`` intentionally: if the table is missing here
    # something unexpected happened and we should surface it immediately.
    op.execute("DROP TABLE IF EXISTS insider_transactions CASCADE")

    # ── 2. Recreate with the correct L-4b schema ────────────────────────────
    # Matches InsiderTransactionModel.__table_args__ exactly.  All types use
