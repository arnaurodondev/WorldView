/**
 * components/ui/separator.tsx — shadcn/ui Separator (Radix UI)
 *
 * WHY THIS EXISTS: Used for visual separation between sections in the sidebar,
 * instrument detail tabs, and settings panels. Uses Radix UI's accessible
 * Separator primitive with aria-orientation support.
 */

"use client";
// WHY "use client": Radix UI Separator uses React context internally

import * as React from "react";
import * as SeparatorPrimitive from "@radix-ui/react-separator";
import { cn } from "@/lib/utils";

const Separator = React.forwardRef<
  React.ElementRef<typeof SeparatorPrimitive.Root>,
  React.ComponentPropsWithoutRef<typeof SeparatorPrimitive.Root>
>(
  (
    { className, orientation = "horizontal", decorative = true, ...props },
    ref,
  ) => (
    <SeparatorPrimitive.Root
      ref={ref}
      decorative={decorative}
      orientation={orientation}
      className={cn(
        "shrink-0 bg-border",
        orientation === "horizontal" ? "h-[1px] w-full" : "h-full w-[1px]",
        className,
      )}
      {...props}
    />
  ),
);
Separator.displayName = SeparatorPrimitive.Root.displayName;

export { Separator };
